import "./App.css";
import { useState } from "react";
import Slider from "react-slick";



import SIDEONE from '../src/assets/SIDEONE.svg';
import SIDETWO from '../src/assets/SIDETWO.svg';
import BRANDLEFT from '../src/assets/BRANDLEFT.svg';
import BRANDFRONT from '../src/assets/BRANDFRONT.svg';
import BRANDRIGHT from '../src/assets/BRANDRIGHT.svg';
import { FaArrowRight, FaArrowLeft } from "react-icons/fa";

import './ImageSlider.css'



const images1 = [BRANDFRONT,SIDETWO, BRANDLEFT, SIDEONE];
const images2 = [SIDEONE, SIDETWO, BRANDLEFT, BRANDFRONT];
const images3 = [SIDEONE,BRANDRIGHT, BRANDLEFT, SIDETWO];


export function Imageslider1() {
  const NextArrow = ({ onClick }) => {
    return (
      <div className="arrow next" onClick={onClick}>
        <FaArrowRight />
      </div>
    );
  };

  const PrevArrow = ({ onClick }) => {
    return (
      <div className="arrow prev" onClick={onClick}>
        <FaArrowLeft />
      </div>
    );
  };

  const [imageIndex, setImageIndex] = useState(0);

  const settings = {
    infinite: true,
    lazyLoad: true,
    speed: 300,
    slidesToShow: 3,
    centerMode: true,
    centerPadding: 0,
    nextArrow: <NextArrow />,
    prevArrow: <PrevArrow />,
    beforeChange: (current, next) => setImageIndex(next),
  };

  return (
    <div className="App">
      <Slider {...settings}>
        {images1.map((img, idx) => (
          <div className={idx === imageIndex ? "slide activeSlide" : "slide"}>
            <img src={img} alt={img} />
          </div>
        ))}
      </Slider>
    </div>
  );
}

